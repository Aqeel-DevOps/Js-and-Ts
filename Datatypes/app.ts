// there are two types of datatypes in javascripts
// 1. Primitive datatypes
// 1.1. string
// string is a sequence of characters and enclosed in inverted commas is called string
// any thing that is enclosed in inverted commas is called string
let myName: string = "John Doe";
// 1.2. number
// number can be integer or float
let myAge: number = 30;
// 1.3. boolean
let isMarried: boolean = false;
// 1.4. Undefined
// value taken by a variable that is not assigned a value is undefined
let myAddress: string;

// let check the typeof the variable
console.log(typeof myAge);//number
console.log(typeof isMarried);//boolean
console.log(typeof myAddress);//undefined









